   (NO_SAP, REGION, ACTIVO, C_PROCESO, PLATAFORMA, EQUIPO, AGRUPADOR, FECHA_CONTABLE, FECHA_REAL, MES, ANIO, TIPO_INSTALACION, ID_EQUIPO, REPORTE)
